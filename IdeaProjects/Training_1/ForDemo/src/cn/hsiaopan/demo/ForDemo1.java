package cn.hsiaopan.demo;

public class ForDemo1 {
    public static void main(String[] args) {
        // 需求： 打印5次HelloWorld
        for(int i = 1; i <= 5; i++){
            System.out.println("Hello World");
            System.out.println(i);
            System.out.println("-----------");
        }
    }
}
