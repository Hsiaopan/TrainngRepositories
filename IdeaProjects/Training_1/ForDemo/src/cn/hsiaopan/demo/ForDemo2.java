package cn.hsiaopan.demo;

public class ForDemo2 {
    public static void main(String[] args) {
        // 需求：在控制台上输出1-5
        for(int i = 1; i <= 5; i++){
            System.out.println(i);
        }

        //      在控制台上输出5-1
        for(int j = 5; j >= 1; j--){
            System.out.println(j);
        }
    }
}
