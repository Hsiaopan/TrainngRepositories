package cn.hsiaopan.demo;

public class IfDemo1 {
    public static void main(String[] args) {
        System.out.println("开始执行");

        // 定义两个变量
        int a = 20;
        int b = 30;

        // 判断两个变量是否相等
        if(a == b){
            System.out.println("a和b是相等的");
        }else{
            System.out.println("a和b是不相等的");
        }


        System.out.println("结束执行");

    }
}
